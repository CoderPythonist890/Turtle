def eye(x0, y0, x=31, y=95):
	color("white")
	goto(x0, y0)
	color("black")
	begin_fill()
	goto(x0, y0 + y)
	goto(x0 - x, y0 + y)
	goto(x0 - x, y0)
	goto(x0, y0)
	end_fill()


